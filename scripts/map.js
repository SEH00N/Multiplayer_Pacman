class Map {
    constructor(tileSize) {
        this.size = new Vector2(0, 0);
        this.tileList = [];

        this.createTiles(tileSize)
    }

    createTiles(tileSize) {
        let mapData = MapData.split('\n');
        this.size.x = mapData[0].length;
        this.size.y = mapData.length;

        for (let x = 0; x < this.size.x; x++) {
            for (let y = 0; y < this.size.y; y++) {
                let xPos = x * tileSize;
                let yPos = y * tileSize;

                let tileType = mapData[x][y];

                this.tileList[x][y] = new GameObject(xPos, yPos, tileSize, tileSize, loadImage(TileType[tileType]));
            }
        }
    }
}

